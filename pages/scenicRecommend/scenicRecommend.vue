<template>
	<view class="playRecommend">
		<view class="cu-bar search fixed" style="background: #ff8532;">
			<view class="search-form round" style="background: rgba(255,255,255,0.2);">
				<!-- 				<text class="cuIcon-search" style="color: #7A7A7A;"></text> -->
				<!-- <view class="address" @click="gotocity()">{{ cityName }}</view>
				<image class="address-img" src="../../static/img/citydown.png" mode=""></image>
				<view class="shu"></view> -->
				<input
					class="search_input"
					type="text"
					style="font-weight: 300;"
					:value="inputVal"
					placeholder="请输入名称进行搜索"
					confirm-type="search"
					@confirm="search()"
					@input="searchIcon"
					placeholder-style="color:#fff"
				/>
			</view>
		</view>
		<view class="topbtn">
			<view class="playRecommend_nav">
				<view class="nav" :class="isSelected === 5 ? 'nav_selected' : ''" @click="navSelect(5)">特产</view>
				<view class="nav" :class="isSelected === 6 ? 'nav_selected' : ''" @click="navSelect(6)">跟团</view>
				<view class="nav" :class="isSelected === 1 ? 'nav_selected' : ''" @click="navSelect(1)">酒店</view>
				<view class="nav" :class="isSelected === 2 ? 'nav_selected' : ''" @click="navSelect(2)">景点</view>
				<view class="nav" :class="isSelected === 3 ? 'nav_selected' : ''" @click="navSelect(3)">餐饮</view>
				<view class="nav" :class="isSelected === 4 ? 'nav_selected' : ''" @click="navSelect(4)">民宿</view>
				<!-- <view class="nav" :class="isSelected === 5 ? 'nav_selected' : ''" @click="navSelect(5)">特色</view> -->
				<!-- <view class="nav" :class="isSelected === 7 ? 'nav_selected' : ''" @click="navSelect(7)">特产</view> -->
			</view>
		</view>

		<view class="list">
			<view class="list_one" v-for="item in list" :key="item" @click="toDetail(item.id, item.type)">
				<image :src="item.cover_image" mode=""></image>
				<view class="list_one_r">
					<view class="title">{{ item.title }}</view>
					<view class="intro">{{ item.description }}</view>
					<view class="introb" v-if="type == 'boutique'">
						<view class="l">目的地：{{ item.destination }}</view>
						<text></text>
						<view class="r">行程天数：{{ item.last_days }}天</view>
					</view>
					<!-- <view class="price" v-show="isSelected != 6 && isSelected != 2">
						门市价:
						<text>￥</text>
						<text class="price">{{ item.purchase_price }}</text>
						<text class="price-right">门市价:￥{{ item.price }}</text>
					</view> -->
					<view class="price">
						<text v-if="type != 'feature'">门市价:</text>
						<text v-if="type == 'feature'">售卖价:</text>
						<text class="text">￥</text>
						<text class="price">{{ item.price }}</text>
						<!-- 门市价:
						<text>￥</text>
						<text class="price">{{ item.purchase_price }}</text> -->
						<!-- <text class="price-right">门市价:￥{{ item.price }}</text> -->
					</view>
				</view>
			</view>
		</view>
		<!--加载loadding-->
		<tui-loadmore :visible="loadding"></tui-loadmore>
		<tui-nomore :visible="!pullUpOn"></tui-nomore>
		<!--加载loadding-->
	</view>
</template>

<script>
import uniIcons from '@/plugins/components/uni-icons/uni-icons.vue';
import tuiLoadmore from '@/plugins/thorui/components/loadmore/loadmore.vue';
import tuiNomore from '@/plugins/thorui/components/nomore/nomore';
import { getResources } from '@/http/api.js';
export default {
	components: {
		uniIcons,
		tuiLoadmore,
		tuiNomore
	},
	data() {
		return {
			list: [],
			inputVal: '',
			keywords: '',
			page: 1,
			cityName: '区域',
			cityCode: '',
			loadding: false,
			pullUpOn: true,
			isSelected: 5,
			type: 'feature'
		};
	},
	onShow() {
		wx.hideHomeButton();
		let pages = getCurrentPages();
		let currPage = pages[pages.length - 1]; //当前页面
		this.cityName = currPage.data.cityName;
		this.cityCode = currPage.data.cityCode;
		if (this.cityName !== '区域') {
			this.list = [];
			this.page = 1;
			this.pullUpOn = true;
			this.getList(this.keywords, this.cityCode);
		}
	},
	onLoad(options) {
		this.id = options.id;
		this.getList();
	},
	methods: {
		gotocity() {
			uni.navigateTo({
				url: '/pages/selectCity/selectCity'
			});
		},
		getList(keywords, cityCode) {
			if (keywords == undefined) {
				this.keywords = '';
			} else {
				this.keywords = keywords;
			}
			if (cityCode == undefined) {
				this.cityCode = '';
			} else {
				this.cityCode = cityCode;
			}
			let destination_code = this.cityCode;
			let city_Code = this.cityCode;
			if (this.type == 'boutique') {
				city_Code = '';
				destination_code = this.cityCode;
			} else {
				city_Code = this.cityCode;
				destination_code = '';
			}
			// let city_Name=this.cityName
			// if(this.cityName=="区域"){
			// 	city_Name=""
			// }else{
			// 	city_Name=this.cityName
			// }

			getResources(this.id, this.page, this.type, this.keywords, city_Code, destination_code).then(res => {
				if (!this.pullUpOn) return;
				this.loadding = true;
				if (res.data.data.length == 0) {
					this.loadding = false;
					this.pullUpOn = false;
					return;
				}
				if (res.data.current_page == res.data.last_page) {
					this.loadding = false;
					this.pullUpOn = false;
					this.list = this.list.concat(res.data.data);
					this.page = this.page + 1;
					return;
				}
				this.list = this.list.concat(res.data.data);
				this.page = this.page + 1;
			});
		},
		searchIcon(e) {
			this.keywords = e.detail.value;
			this.inputVal = e.detail.value;
		},
		search() {
			this.page = 1;
			this.list = [];
			this.pullUpOn = true;
			this.getList(this.keywords);
		},
		toDetail(id, type) {
			switch (type) {
				case 'hotel':
					uni.navigateTo({
						url: `/pages/details/hotelDetail/hotelDetail?id=${id}&isDis=1`
					});
					break;
				case 'repast':
					uni.navigateTo({
						url: `/pages/details/restaurantDetail/restaurantDetail?id=${id}&isDis=1`
					});
					break;
				case 'sight':
					uni.navigateTo({
						url: `/pages/details/scenicSpotDetail/scenicSpotDetail?id=${id}&isDis=1`
					});
					break;
				case 'homestay':
					uni.navigateTo({
						url: `/pages/details/homestayDetail/homestayDetail?id=${id}&isDis=1`
					});
					break;
				case 'feature':
					uni.navigateTo({
						url: `/pages/details/otherDetail/otherDetail?id=${id}&isDis=1`
					});
					break;
				case 'boutique':
					uni.navigateTo({
						url: `/pages/details/standardDetail/standardDetail?id=${id}&isDis=1`
					});
					break;
				case 'specialty':
					uni.navigateTo({
						url: `/pages/details/specialtyDetail/specialtyDetail?id=${id}&isDis=1`
					});
					break;
			}
		},
		navSelect(index) {
			this.page = 1;
			this.list = [];
			this.inputVal = '';
			this.pullUpOn = true;
			this.isSelected = index;
			if (index === 1) {
				this.type = 'hotel';
			}
			if (index === 2) {
				this.type = 'sight';
			}
			if (index === 3) {
				this.type = 'repast';
			}
			if (index === 4) {
				this.type = 'homestay';
			}
			if (index === 5) {
				this.type = 'feature';
			}
			if (index === 6) {
				this.type = 'boutique';
			}
			if (index === 7) {
				this.type = 'specialty';
			}
			this.getList(this.keywords, this.cityCode);
		}
	},
	// 页面上拉触底事件的处理函数
	onReachBottom: function() {
		this.getList(this.keywords);
	}
};
</script>

